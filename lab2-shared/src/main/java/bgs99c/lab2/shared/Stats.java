package bgs99c.lab2.shared;

/**
 * Stats of a fighter
 */
public enum Stats {
    /**
     * Amount of health points
     */
    HEALTH,
    /**
     * Ability to resist raw damage
     */
    DEFENCE,
    /**
     * Ability not to miss opponent with attacks
     */
    ACCURACY,
    /**
     * Ability to evade opponent's attacks
     */
    EVASION,
    /**
     * Ability to deal raw damage
     */
    POWER
}
